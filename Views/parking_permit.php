           Doctor of Osteopathy fill out the “temporary disability” Medical Certification
            section in Part 2 of the application form and return the form to you. NOTE:
             If you have a permanent disability, the need for medical certification may be
              waived by the issuing agent if you have an obvious, visually-identifiable
               disability (such as the loss of a leg) OR if you already have license plates
                for persons with severe disabilities.</p>
   <p>You must take your completed application to the appointed issuing agent for the city,
