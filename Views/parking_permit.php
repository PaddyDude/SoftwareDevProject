      required for an original registration and include proof of your disability.</p>
    <p> - If you have plates on your car, bring the plates with you to exchange for plates
       for persons with severe disabilities. You must fill out a registration application
       (form MV-82) and pay $25.00 for the new plates that show the International Symbol of
       Access (ISA).</p>
    <p> - If this transaction is done at any time other than when you renew a vehicle registration,
       you will have to pay an additional $3.75 transaction fee.</p>
