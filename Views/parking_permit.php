    <p>2) To apply for license plates:</p>
    <p>Your disability must be permanent.</p>
    <p>The vehicle on which the special plates will be used must be registered to the person
       with the disability. A person must be at least age 16 to have a vehicle registered
       in his or her name.</p>
    <p>Fill out Part 1 of the application on page 3. A Medical Doctor, Doctor of Osteopathy,
       Physician Assistant, Nurse Practitioner, Doctor of Podiatric Medicine (for disabilities
        related to the foot) or Optometrist (for blindness) must fill out the “Permanent
        Disability” section in Part 2 of the application form and return the form to you.
        NOTE: The need for a medical certification may be waived by the Motor Vehicles office
        if you are permanently disabled and have an obvious, visually-identifiable disability
        (such as the loss of a leg) OR if you have a permanent (blue) parking permit for
        the disabled. Permit holders should bring the permit with them and, also, fill out
        Part 1 of the application and attach a copy of the permit application showing the
        medical certification or doctor’s statement.</p>
    <p>You can get the plates at any Motor Vehicles office. Bring the completed application
      with you.</p>
