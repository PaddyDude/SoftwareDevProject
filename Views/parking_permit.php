        or a non-driver ID card that is issued by the NYS DMV, you must show the document to
         the issuing agent. The issuing agent will write on your permit the last three digits
          of the nine-digit number that is printed on your license or non-driver ID card to
           help law enforcement identify the actual permit holder and help limit abuse. YOU
            ARE NOT REQUIRED TO HAVE A DRIVER  LICENSE OR NON-DRIVER ID ISSUED BY THE NYS DMV
