    <h3>USING LICENSE PLATES AND PARKING PERMITS</h3>
    <p>The plates and permits may be used to park in reserved parking spaces only when the
       person with the disability rides in or drives the vehicle. People who are not disabled
        cannot use your parking permit or special plates to park in a reserved space;
         persons who park illegally in these spaces violate Section 1203-c(4) of the NYS
          Vehicle and Traffic Law and can be fined $50 to $75 for a first offense, and
           $75 to $150 for a second offense that is committed within two years.
            Municipalities can impose higher fines. Any person who abuses or misuses any
             parking permit or license plates for the disabled may have the permit or plates
              revoked.</p>
    <p>Parking permits should be hung from the rear-view mirror when the vehicle is parked,
      but should be removed from the mirror when the vehicle is driven.</p>
    <p>Generally, the plates and permits are valid everywhere in New York State where parking
       spaces are reserved for persons with disabilities. However, use of the plates or
        permit does not exempt you from state or local parking regulations or fees or the
         requirement to obtain permission to park in a designated area. The plates and
         permits are also valid in most other states, territories and foreign countries.
         If you will travel outside of New York, check with the police or Motor Vehicles
         agency in the location where you will be to be sure your permit or plate will be
         accepted.</p>
    <p>Parking spaces reserved for people with disabilities must be marked with conspicuous
      and permanently installed above-grade signs that display the wheelchair symbol. These
      signs are positioned at a height of five to seven feet above the parking space surface.
      Designation of reserved spaces may include the use of blue painted lines or markings.
      Do not park in the striped access aisle next to spaces reserved for people with
      disabilities even if you have a permit or plates. Access aisles provide room for
      people with wheelchairs and other specially equipped vehicles to transfer safely to
      and from their vehicles.</p>
    <p>Localities designate parking spaces for persons with disabilities by local law or
      ordinance. If you have a question about parking for the disabled on a particular street,
       contact the authority that maintains the road. If you have a question about reserved
        parking at any facility with off-street parking, contact the facility manager or the
         local building inspector.</p>
    <h3>PARKING IN NEW YORK CITY</h3>
    <p>New York City (NYC) does not reserve spaces on its streets solely for persons with
      disabilities. However, the City does issue a City permit (a rectangular dashboard
      permit) that allows a person with severe disabilities to park at City curbside
      spaces that are not available to most vehicles, or at legal metered spaces without
      charge. This permit is not valid outside of New York City. You can have a City permit
       and a New York State permit, which are issued by the NYC DOT.</p>
    <p>Throughout New York State and in New York City, reserved parking spaces are available
       in parking lots for shopping centers, malls, office buildings, apartment buildings,
        and college campuses. You can use the special plates or the State permit to park
         in designated spaces in those off-street lots.</p>
    <p>For more information or to get a permit application, you can visit the NYC DOT website
       at www.nyc.gov/dot or call (718) 433-3100. You may also contact the NYC DOT at:</p>
    <p>NYC Department of Transportation </br> Permits and Customer Service </br> 30-30
     Thomson Avenue, 2nd Floor </br> Long Island City, NY 11101-3045</p>
    <h3>METERED PARKING WAIVERS</h3>
    <p>If you have a mobility-related disability and a certain severe disability that limits
       your ability to access or put payment into a parking meter, you may be eligible
       for a metered parking waiver. The metered parking waiver allows the holder to park
        in a metered parking space in any city, town or village of New York State (except
        in New York City) without paying the fee. For more information, see forms MV-664.1MP
        (Application for a Metered Parking Waiver for Persons with Severe Disabilities)
        and MV-664.2MP (Metered Parking Waiver Information). These forms are available
        through your local issuing agent and are also available on the DMV website at
        dmv.ny.gov.</p>
